import math


def calculate_sin(angle: int) -> float:
    return math.sin(angle)


def calculate_cos(angle: int) -> float:
    return math.cos(angle)


def calculate_tan(angle: int) -> float:
    return math.tan(angle)


def calculate_ctg(angle: int) -> float:
    return math.cos(angle) / math.sin(angle)
